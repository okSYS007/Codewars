# Для решения этой задачи вам нужно написать программу, 
# которая определяет, кто съел последнее печенье. 
# Если на вход подается строка, то печенье съел "Зак". 
# Если на вход подается число с плавающей запятой или целое число, 
# то печенье съела "Моника". Если на вход подается что-либо другое, 
# то печенье съела "собака". Результат должен выглядеть так: "Кто съел последнее печенье? Это был (имя)!"

# Пример: Ввод = "привет" --> Вывод = "Кто съел последнее печенье? Это был Зак!" 
# (Причина, по которой возвращается "Зак", заключается в том, что ввод — это строка)

# Примечание: Убедитесь, что вы возвращаете правильное сообщение с правильными пробелами и знаками препинания.

# Пожалуйста, оставьте отзыв об этой ката. Спасибо!

def cookie(x):
    if isinstance(x, str):
        eater = "Zach"
    elif isinstance(x, (int, float)) and not isinstance(x, bool):
        eater = "Monica"
    else:
        eater = "the dog"
    return f"Who ate the last cookie? It was {eater}!"